number1 = 18
number2 = 2

add = number1 + number2 
print(add) # add between numbers
print(type(add))

substract = number1 - number2
print(substract) # substract between numbers
print(type(substract))

multiply = number1 * number2
print(multiply) # multiply between numbers
print(type(multiply))

divide = number1 / number2
print(divide) # devide between numbers
print(type(divide)) # return float type

mod = number1 % number2
print(mod) # return remainder of the division
print(type(mod))

expo = number1 ** number2
print(expo) # raising number to the power of other.
print(type(expo))

fd = number1 // 7
print(fd) # return the lower number point
print(type(fd)) # return float type