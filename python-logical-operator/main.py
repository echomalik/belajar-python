number1 = 15
number2 = 14

operator_and = number1 & number2
print(f"{number1} & {number2} = {operator_and}")

operator_or = number1 | number2
print(f"{number1} | {number2} = {operator_or}")

operator_not = ~number1
print(f"~{number1} = {operator_not}")

operator_xor = number1^number2
print(f"{number1} ^ {number2} = {operator_xor}")