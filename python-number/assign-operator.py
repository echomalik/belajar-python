number += 2 # 8 + 2 = 10
print(number)

number -= 2 # 10 - 2 = 8
print(number)

number *= 2 # 8 * 2 = 16
print(number)

number /= 2 # 16 / 2 = 8.0
print(number)

number %= 2 # 8 % 2 = 0
print(number)

