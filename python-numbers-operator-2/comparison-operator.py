# Comparison Operators

number1 = 14
number2 = 6

# == (equal)
print(number1 == number2)

# != (not equal)
print(number1 != number2)

# > (greater than)
print(number1 > number2)

# < (less than)
print(number1 < number2)

# >= (greater than equal)
print(number1 >= number2)

# <= (less than equal)
print(number1 <= number2)